for i in range(1, 21):
    if i % 2 == 0:
        print(i)
# break statement        
for i in range(1, 10):
    if i == 5:
        break
    print("break when equal to 5:",i)
# continue statement    
for i in range(1, 6):
    if i == 3:
        continue
    print("skip when equal to 3:",i)
